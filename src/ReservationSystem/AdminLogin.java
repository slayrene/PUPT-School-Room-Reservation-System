    /**
    * Authenticates the admin user based on the entered username and password.
    */   
    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed

        String uN = username.getText();
        String pS = String.valueOf(password.getPassword());
        if (uN.trim().equals("")){
            jOptionPane1.showMessageDialog(rootPane, "Enter your username to Login", "Empty Username", 2);
        } else if (pS.trim().equals("")) {
            jOptionPane1.showMessageDialog(rootPane, "Enter your password to Login", "Empty Password", 2);
        } else {
            try {
                
                String sql = "SELECT * FROM admin WHERE admin = ? AND password = ?";
                pst = con.prepareCall(sql);
                pst.setString(1, uN);
                pst.setString(2, pS);
                
                rs = pst.executeQuery();

                if (rs.next()) {
                    User user = new User(uN);
                    AdminHomePage homePageFrame = new AdminHomePage(user, this);
                    homePageFrame.setVisible(true);
                    homePageFrame.pack();
                    homePageFrame.setLocationRelativeTo(null);
                    this.dispose();                
                    
                } else {
                    jOptionPane1.showMessageDialog(rootPane, "Wrong Username or Password", "Login Failed!", 2);
                }
            } catch (Exception e) {
                e.printStackTrace(); // Add proper error handling here
            }
        }
    }//GEN-LAST:event_loginButtonActionPerformed

    /**
     * This method handles the key press event on the "Login" button.
     * It checks if the pressed key is Enter (VK_ENTER). If yes, it retrieves the username from the `username` text field and the password from the `password` password field.
     * It then attempts to connect to the database and execute a prepared statement to check for a matching admin record with the provided username and password.
     * - If a match is found, a new `User` object is created with the username and a new `AdminHomePage` frame is opened, passing the user and a reference to this frame. The new frame is made visible, packed, centered, and this frame is hidden.
     * - If no match is found, a login failed message is displayed using a JOptionPane dialog.
     * In case of any exceptions during the process, it catches the exception but doesn't perform any specific action (consider adding logging or error handling as needed).
     *
     * @param evt The `KeyEvent` object generated by the key press on the button.
     */
    private void loginButtonKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_loginButtonKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String uN = username.getText();
            String pS = String.valueOf(password.getPassword());

            try {
                String sql = " SELECT * FROM admin WHERE admin = ? AND password = ?";
                pst = con.prepareCall(sql);
                pst.setString(1, uN);
                pst.setString(2, pS);

                rs = pst.executeQuery();

                if (rs.next()) {
                    User user = new User(uN);
                    AdminHomePage homePageFrame = new AdminHomePage(user, this);
                    homePageFrame.setVisible(true);
                    homePageFrame.pack();
                    homePageFrame.setLocationRelativeTo(null);
                    this.setVisible(false);   
                } else {
                    jOptionPane1.showMessageDialog(rootPane, "Login Failed!");
                }
            } catch (HeadlessException | SQLException e){

            }
        }
    }//GEN-LAST:event_loginButtonKeyPressed

