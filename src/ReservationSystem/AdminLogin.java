    /**
     * This method handles the action event triggered when the "Show Password" checkbox is clicked.
     * It toggles the visibility of the password characters in the `password` field.
     * - If the checkbox is checked (selected), the password field's echo character is set to a bullet point ('•') to hide the password.
     * - If the checkbox is unchecked (deselected), the password field's echo character is set to null ((char) 0) to show the password characters.
     * The `isPasswordVisible` boolean flag is updated to reflect the current state.
     *
     * @param evt The `ActionEvent` object generated by the checkbox click.
     */
    private void showPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showPasswordActionPerformed
        if (isPasswordVisible) {
            password.setEchoChar('•'); // Hide password
        } else {
            password.setEchoChar((char) 0); // Show password
        }
        isPasswordVisible = !isPasswordVisible;
    }//GEN-LAST:event_showPasswordActionPerformed

