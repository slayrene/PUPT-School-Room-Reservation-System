    /**
     * Handles the login button click event.
     * Performs user authentication, opens the User Home window on success,
     * displays error messages on failure.
     *
     * @param evt the ActionEvent object generated by the button click
     */
    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed

        String uN = username.getText();
        String pS = String.valueOf(password.getPassword());        
        if (uN.trim().equals("")){
            jOptionPane1.showMessageDialog(rootPane, "Enter your username to Login", "Empty Username", 2);
        } else if (pS.trim().equals("")) {
            jOptionPane1.showMessageDialog(rootPane, "Enter your password to Login", "Empty Password", 2);
        } else {
            try {
                String sql = " SELECT * FROM tblstudent WHERE studno = ? AND password = ?";
                pst = con.prepareCall(sql);
                pst.setString(1, uN);
                pst.setString(2, pS);
            
                rs = pst.executeQuery();         
            
                if (rs.next()) {
                    User user = new User(uN);
                    UserHome userHomeFrame = new UserHome(user, this);
                    userHomeFrame.setVisible(true);
                    userHomeFrame.pack();
                    userHomeFrame.setLocationRelativeTo(null);
                    this.setVisible(false);
                } else {
                     jOptionPane1.showMessageDialog(rootPane, "Wrong Username or Password","Login Failed!", 2);
                }
            } catch (HeadlessException | SQLException e){

            }   
        }        
    }//GEN-LAST:event_loginButtonActionPerformed
    /**
     * Handles the Enter key press event on the login button.
     * Similar functionality to loginButtonActionPerformed but triggered by Enter key.
     *
     * @param evt the KeyEvent object generated by the Enter key press
     */
    private void loginButtonKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_loginButtonKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String uN = username.getText();
            String pS = password.getText();        
        
            try {
                String sql = " SELECT * FROM tblstudent WHERE studno = ? AND password = ?";
                pst = con.prepareCall(sql);
                pst.setString(1, uN);
                pst.setString(2, pS);

                rs = pst.executeQuery();         

                if (rs.next()) {
                    User user = new User(uN);
                    UserHome userHomeFrame = new UserHome(user, this);
                    userHomeFrame.setVisible(true);
                    userHomeFrame.pack();
                    userHomeFrame.setLocationRelativeTo(null);
//                    this.dispose();   
                    this.setVisible(false);   
                } else {
                     jOptionPane1.showMessageDialog(rootPane, "Login Failed!");
                }
            } catch (HeadlessException | SQLException e){

            }
        }
    }//GEN-LAST:event_loginButtonKeyPressed
